// WHO Growth Reference - Boys 5-12 months
// [month, P3w, P15w, P50w, P85w, P97w, P3h, P15h, P50h, P85h, P97h]
export const WHO = [
  [5, 6.0,6.7,7.5,8.4,9.3, 61.7,63.5,65.9,68.3,70.1],
  [6, 6.4,7.1,7.9,8.8,9.8, 63.3,65.2,67.6,70.0,72.0],
  [7, 6.7,7.4,8.3,9.2,10.2,65.1,67.0,69.2,71.6,73.5],
  [8, 7.0,7.7,8.6,9.6,10.7,66.5,68.4,71.0,73.5,75.5],
  [9, 7.2,8.0,8.9,9.9,11.0,68.0,70.0,72.3,74.8,76.9],
  [10,7.5,8.2,9.2,10.2,11.4,69.4,71.4,73.8,76.3,78.4],
  [11,7.7,8.4,9.4,10.5,11.7,70.8,72.8,75.3,77.8,79.9],
  [12,7.8,8.6,9.6,10.8,12.0,72.1,74.2,76.6,79.2,81.3],
];

export function whoAssess(month, w, h) {
  const row = WHO.find(r => r[0] === month);
  if (!row) return {};
  const [,p3w,p15w,p50w,p85w,p97w,p3h,p15h,p50h,p85h,p97h] = row;
  const wPct = w<p3w?'<P3':w<p15w?'P3-15':w<p50w?'P15-50':w<p85w?'P50-85':w<p97w?'P85-97':'>P97';
  const hPct = h<p3h?'<P3':h<p15h?'P3-15':h<p50h?'P15-50':h<p85h?'P50-85':h<p97h?'P85-97':'>P97';
  const wOk = w>=p3w && w<=p97w;
  const hOk = h>=p3h && h<=p97h;
  return {wPct, hPct, wOk, hOk, p3w, p50w, p97w, p3h, p50h, p97h};
}
